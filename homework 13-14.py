# Користувач вводить із клавіатури два числа (початок і кінець діапазону).
# Потрібно проаналізувати всі числа в цьому діапазоні.
# Виведення на екран має відбуватися за правилами, зазначеними нижче.
# Якщо число кратне 3 (ділиться на 3 без залишку) потрібно вивести слово "Fizz".
# Якщо число кратне 5 потрібно вивести слово "Buzz".
# Якщо число кратне 3 і 5 потрібно вивести "Fizz Buzz". Якщо число не кратне не 3 і 5 потрібно вивести саме число.

start = int(input("Введіть початок діапазону: "))
end = int(input("Введіть кінець діапазону: "))
current = start
while current <= end:
    if current % 3 == 0 and current % 5 == 0:
        print("Fizz Buzz")
    elif current % 3 == 0:
        print("Fizz")
    elif current % 5 == 0:
        print("Buzz")
    else:
        print(current)

    current += 1


# Користувач вводить два числа і крок (інтервал), з яким потрібно проходити по діапазону.
# Програма має показати числа від початку діапазону до кінця, збільшуючи кожне число на вказаний крок.
# Також програма повинна надавати вибір порядку виведення: у прямому або зворотному порядку.

start = int(input("Введіть початок діапазону: "))
end = int(input("Введіть кінець діапазону: "))
step = int(input("Введіть крок: "))
order = int(input("Оберіть порядок виведення (1 — прямий, 2 — зворотний): "))
if step <= 0:
    print("Крок повинен бути додатним числом")
else:
    if order == 1:
        current = start
        while current <= end:
            print(current)
            current += step
    elif order == 2:
        current = end
        while current >= start:
            print(current)
            current -= step
    else:
        print("Невірно обраний порядок виведення")


# Користувач вводить два числа, що представляють діапазон.
# Програма повинна пройти по діапазону і вивести добуток усіх чисел, які діляться на 4, але не діляться на 6.
# Якщо таких чисел немає, вивести відповідне повідомлення.
# Діапазон має автоматично нормалізуватися, якщо початок більший за кінець.

start = int(input("Введіть початок діапазону: "))
end = int(input("Введіть кінець діапазону: "))
if start > end:
    start, end = end, start
result = 1
has_numbers = False

for i in range(start, end + 1):
    if i % 4 == 0 and i % 6 != 0:
        result *= i
        has_numbers = True

if has_numbers:
    print("Добуток чисел:", result)
else:
    print("У діапазоні немає чисел, які діляться на 4, але не діляться на 6.")


# Користувач вводить два числа: число A і ступінь N, у який потрібно піднести число.
# Програма повинна обчислити A у степені N за допомогою циклу
# (без використання вбудованих функцій піднесення до степеня)

A = int(input("Введіть число A: "))
N = int(input("Введіть ступінь N: "))
result = 1
i = 0
while i < N:
    result = result * A
    i = i + 1
print("Результат:", result)